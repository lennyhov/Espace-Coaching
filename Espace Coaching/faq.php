			<h1>Questions fréquemment posées :</h1>
			<h1>Faq coach:</h1>
			
			<h2>Quel est l'intérêt de ce site pour moi qui suis coach ?</h2>
			<p>Espace Coaching est la première plateforme qui permet aux coachs de communiquer sur leurs services, de gagner en visibilité et d'aller à la rencontre de clients potentiels. L'intérêt est donc d'abord de figurer dans l'annuaire du coaching sportif le plus important jamais créé en France.
De plus vous avez la possibilité de démontrer vos connaissances et votre savoir-faire via du contenu que vous pouvez publier librement. Les clients sont sensibles à l’activité des coachs et vous pourrez gagner leur confiance en affichant vos compétences.
</p>
			
			<h2>Puis je m'inscrire moi-même sur le site?</h2>
			<p>Oui vous pouvez vous inscrire par vous-même sur la plateforme espace coaching pour proposer vos services, pour cela rendez-vous dans la rubrique inscription (Lien hypertexte).</p>
			
			<h2>Figurer dans l'annuaire est-il gratuit ?</h2>
			<p>Oui vous serez référencé dans notre annuaire des coachs de manière 100% gratuite. Lorsque la plupart des plateformes d'annonces vous demanderont des frais pour être référencés, vous pourrez sur espace coaching accroître votre clientèle sans dépenses supplémentaires.</p>
			
			<h2>Peut-on figurer dans la liste de plusieurs départements?</h2>
			<p>Oui il est possible de figurer dans le répertoire de 1 à 3 départements.</p>
			
			<h2>Comment puis-je améliorer mon classement dans la liste de recherche?</h2>
			<p>Le classement des coachs par département suit une règle simple. Les coachs les plus actifs sur la plateforme (articles, vidéos, conseils) et recevant les meilleures notations pour la qualité de leurs travaux sont les mieux classés. Ainsi pour gagner des places et donc gagner en visibilité nous vous conseillons la rédaction fréquente de contenu dans la catégorie conseils de coachs!</p>
			
			<h2>Comment rédiger des articles sur le site ?</h2>
			<p>Vous pouvez publier du contenu directement depuis votre espace coach personnel. Une rubrique est dédiée à la rédaction d'articles que vous pourrez facilement soumettre à la publication</p>
			
			<h2>Comment poster des photos et vidéos?</h2>
			<p>Vous pouvez poster vos photos directement depuis votre espace personnel. L'upload se fait très simplement en choisissant parmi vos fichiers les images que vous souhaitez publier.</p>
			
			<h2>Si je ne souhaite plus figurer dans l'annuaire puis-je supprimer ma fiche profil ?</h2>
			<p>Oui, il vous est possible à tout moment de supprimer votre compte et votre page profil. Vous disposez dans votre espace personnel d'une option "supprimer mon compte"</p>
			
			
			<h2>Comment les clients peuvent-ils me contacter?</h2>
			<p>Les clients pourront vous envoyer directement des messages sur votre boîte de messagerie privée. Vous serez notifiés par email lorsqu'un client cherche à vous contacter. Vous pouvez par ailleurs inscrire votre numéro de téléphone sur votre espace profil pour permettre aux intéressés de vous contacter directement de façon encore plus rapide.</p>
			
			
			<h1>Faq client:</h1>
			
			
			<h2>Quel est le concept du site?</h2>
			<p>Espace Coaching est une plateforme de mise en relation entre coachs sportifs et clients potentiels. Il est difficile pour les coachs sportifs de se faire  connaître de leur clientèle et nous souhaitons leur offrir la visibilité dont ils manquent. 
Pour les clients il s'agit d'avoir le choix parmi un grand nombre de coachs disponibles près de chez eux. Vous découvrez les coachs via leur fiche profil, leurs diplômes, expériences mais également contenus publiés, de ce fait il vous est plus facile de reconnaître celui qui vous conviendra pour atteindre vos objectifs de forme et santé.
</p>
			
			<h2>Comment trouver un coach près de chez moi ?</h2>
			<p>Pour cela il vous suffit de lancer une recherche dans notre annuaire des coachs. Ceci peut s'effectuer directement sur la page d'accueil via la rubrique "recherche rapide" ou via la catégorie dédiée à la recherche de coachs. Vous pourrez préciser votre département et accéder facilement à la liste des coachs disponibles proche de chez vous.</p>
			
			<h2>Comment faire mon choix parmi tous les coachs présents?</h2>
			<p>Une fois la recherche lancée vous aurez accès à une base de coachs potentiels importante. Plusieurs éléments pourront vous aider à faire votre choix. Vous pourrez d'abord cibler les coachs dont le tarif correspond à vos attentes, vous pourrez ensuite vous fier aux diplômes obtenus, signifiant un certain degré d'apprentissage. 
Enfin Espace Coaching permet aux coachs de démontrer leurs savoir-faire via du contenu de qualité, consultez ainsi leurs articles et vidéos postées pour vous aider à faire votre choix !
</p>
			
			<h2>La mise en relation avec les coachs est-elle gratuite?</h2>
			<p>La mise en relation est totalement gratuite, vous disposez d'une messagerie privée prévue à cet effet. Vous pouvez contacter plusieurs coachs à la fois afin de vous assurer d'obtenir au moins une réponse positive à votre requête</p>
			
			<h2>Comment savoir si un coach est disponible pour une séance ?</h2>
			<p>Vous pouvez consulter directement le coach qui vous intéresse par un envoi de message sur sa fiche profil ou en composant son numéro de téléphone (sous réserve que celui-ci accepte de le communiquer). Vous pourrez ainsi connaître ses disponibilités pour une séance de coaching.</p>
			
			<h2>Les frais de déplacement des coachs sont-ils compris dans l'offre?</h2>
			<p>Oui, les frais de déplacement des coachs sont généralement compris dans le tarif qu'ils indiquent. En admettant bien sûr que vous demandiez au coach d'intervenir dans sa zone d'exercice. Si vous prévoyez un déplacement plus éloigné de sa part, vous pourrez discuter ensemble des éventuels surcoûts à imputer.</p>
			
			<h2>Je n'ai pas le temps de contacter les coachs moi-même, comment faire?</h2>
			<p>Vous pouvez nous demander directement de sélectionner un coach pour vous en nous adressant un e-mail. Nous sélectionnerons alors pour vous un coach disponible dans votre zone et nous chargerons de vous l'envoyer. Pour nous contacter: espace-coaching@gmail.com</p>
			
			<h2>Si le coach manque le rendez-vous comment cela se passe-t-il ?</h2>
			<p>Si le coach ne se présente pas, vous pourrez nous le signaler afin que nous puissions déterminer les raisons de cette absence. La séance prévue ne vous sera évidemment pas facturée par le coach. Vous pourrez alors choisir de planifier à nouveau un rendez-vous avec le même coach ou un nouveau que vous choisirez.</p>
			
			
			
			

						
			
		
		
